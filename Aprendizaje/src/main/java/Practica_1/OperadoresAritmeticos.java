package Practica_1;

import static java.lang.System.out;

public class OperadoresAritmeticos {
    public static void main(String[] args) {
        int Numero1 = 10;
        int Numero2 = 20;
        float Numero3 = 10;
        float Numero4 = 20;

        int Resultado_Sum = Numero2 + Numero1;
        int Resultado_Res = Numero1 - Numero2;
        int Resultado_Mul = Numero1*Numero2;
        int Resultado_Div = Numero2/Numero1;
        float Resultado_Div2 = Numero3/Numero4;
        out.println(Resultado_Sum);
        out.println(Resultado_Res);
        out.println(Resultado_Mul);
        out.println(Resultado_Div);
        out.println(Resultado_Div2);
    }
}
